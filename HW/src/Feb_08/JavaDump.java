package Feb_08;

public class JavaDump {

	public static void main(String[] args) 
	{
		int num1 =10, num2=20;
		int num3=5, num4=7;
		
	
		
		
		System.out.println(num1++);
		System.out.println(num1++);
		System.out.println(num1);
        System.out.println(++num2); 
		System.out.println(num2); 
		System.out.println(num3--); 
		System.out.println(num3); 
		System.out.println(--num4); 
		System.out.println(num4);
		
		 
		
	    
	}

}
